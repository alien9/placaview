    def set_signs_style(self, filter=[], layer=None, size=6):
        if layer is None:
            layer = self.get_point_layer_by_name("traffic signs")
        if self.conf.get("layer_filter"):
            lr=self.get_boundary_by_name(self.conf.get("layer_filter"))
        signs_layer = layer
        idx = signs_layer.fields().indexOf('value_code_face')
        values = list(signs_layer.uniqueValues(idx))
        categories = []
        for value in values:
            if not value:
                v="unknown_sign.svg"
            else:
                v=value
            style = {
                "name": os.path.join(self.plugin_dir,"styles", v),
                'size': size
            }
            symbol = QgsSymbol.defaultSymbol(layer.geometryType())
            symbol.deleteSymbolLayer(0)
            symbol.appendSymbolLayer(QgsSvgMarkerSymbolLayer.create(style))
            category = QgsRendererCategory(v, symbol, str(v))
            if value not in filter:
                category.setRenderState(False)
            else:
                category.setRenderState(True)
            categories.append(category)
    
        renderer = QgsCategorizedSymbolRenderer('value_code_face', categories)
        layer.setRenderer(renderer)
        layer.triggerRepaint()


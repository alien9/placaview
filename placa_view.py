    def group_layers(self):
        root = QgsProject.instance().layerTreeRoot()
        group = root.findGroup("Signs Database")
        if not group:
            group = root.addGroup("Signs Database")
        for layer in (self.get_signs_layer(),self.get_signs_photo_layer(), self.get_selected_sign_layer):
            mylayer = root.findLayer(layer.id())
            myClone = mylayer.clone()
            parent = mylayer.parent()
            group.insertChildNode(0, myClone)
            parent.removeChildNode(mylayer)        

    def deg2num(self, lat_deg, lon_deg, zoom):
        lat_rad = math.radians(lat_deg)
        n = 2.0 ** zoom
        xtile = int((lon_deg + 180.0) / 360.0 * n)
        ytile = int((1.0 - math.asinh(math.tan(lat_rad)) / math.pi) / 2.0 * n)
        return (xtile, ytile)

    def save_conf(self):
        self.check_path()
        patty=f'{QgsProject.instance().fileName()}_data/'
        con=f"{patty}conf.json"
        with open(f"{patty}conf.json", "w+") as fu:
            fu.write(json.dumps(self.conf))
            fu.close()

    def set_conf(self, key, value):
        if not self.conf:
            self.load_conf()
        self.conf[key] = value
        self.save_conf()

    def load_conf(self):
        if QgsProject.instance().fileName()=='':
            self.iface.messageBar().pushMessage("Error", "Please save the project before starting the task.")
            return False
        self.check_path()
        self.conf = {}
        con = os.path.join(self.plugin_dir, "conf.json")
        patty=f'{QgsProject.instance().fileName()}_data/'
        if os.path.isfile(os.path.join(self.plugin_dir, "conf.json")):
            if not os.path.isfile(f"{patty}conf.json"):
                shutil.copy(os.path.join(self.plugin_dir, "conf.json"),f"{patty}conf.json")
        con=f"{patty}/conf.json"
        if os.path.isfile(con):
            with open(con, "r") as fu:
                self.conf = json.loads(fu.read())
                fu.close()
        if self.conf.get("boundary", False):
            self.boundary = self.get_boundary_by_name(
                self.conf.get("boundary"))
        if self.conf.get("roads", False):
            self.roads_layer = self.get_line_by_name(
                self.conf.get("roads"))
        self.roads_field_name = self.conf.get("roads_field_name", False)
        return True

